/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
public class Solution {

    /**
     * 1、基于二叉搜索树以中序遍历是递增序列的规则来递归。
     * 2、当发现某个节点值为key时：
     * ----- 将其替换为null，同时判断其左右子树
     * ---------- 如果右子树为空，那就直接将左子树接上来。
     * ---------- 如果右子树不为空，将右子树接上来。
     * ---------------  原来的子树如果有左子树，那么要将原来的左子树接在右子树（该右子树如果没有左子树就直接接在该右子树的左孩子节点上）的中序遍历序列的第一个节点下面。
     *
     * @param root
     * @param key
     * @return
     */
    public TreeNode deleteNode(TreeNode root, int key) {
        if (root == null) {
            return null;
        }
        if (root.val > key) {
            root.left = deleteNode(root.left, key);
        } else if (root.val < key) {
            root.right = deleteNode(root.right, key);
        } else {
            if (root.right == null) {
                root = root.left;
            } else {
                TreeNode newTree = root.right;
                while (newTree.left != null) {
                    newTree = newTree.left;
                }
                newTree.left = root.left;
                root = root.right;
            }
        }
        return root;
    }

    private static class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode() {
        }

        TreeNode(int val) {
            this.val = val;
        }

        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }

}
